fun main() {
//    Дано целое число. Если оно является положительным, то прибавить к нему 1;
//    в противном случае не изменять его. Вывести полученное число.
    var x = 5
    if (x > 0) {
        x++
    }
    println("Задание 1")
    println(x)

//    Даны целые числа: 4, 45, -2, 8. Найти количество положительных чисел в исходном наборе.
    var count = 0
    val array = arrayOf(4, 45, -2, 8)
    for (i in array) if (i > 0) {
        count += 1
    }
    println("Задание 2")
    println(count)

//    Даны два числа. Вывести большее из них.
    val y = 92
    val z = 9
    println("Задание 3")
    if (y > z) {
        println(y)
    } else {
        println(z)
    }
//    Дано целое число в диапазоне 1–7.
//    Вывести строку — название дня недели, соответствующее данному числу
//    (1 — «понедельник», 2 — «вторник» и т. д.).
    println("Задание 4")
    val day = 2
    when (day) {
        1 -> println("понедельник")
        2 -> println("вторник")
        3 -> println("среда")
        4 -> println("четверг")
        5 -> println("пятница")
        6 -> println("суббота")
        7 -> println("воскресенье")
    }
