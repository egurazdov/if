//    (1 — «плохо», 2 — «неудовлетворительно», 3 — «удовлетворительно», 4 — «хорошо», 5 — «отлично»).
//    Если не лежит в диапазоне 1–5, то вывести строку «ошибка».
    println("Задание 5")
