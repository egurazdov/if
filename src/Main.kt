    }
//    Арифметические действия над числами пронумерованы следующим образом:
//    1 — сложение, 2 — вычитание, 3 — умножение, 4 — деление. Дан номер действия (целое число в диапазоне 1–4) и числа типа double(не равно 0). Выполнить над числами указанное действие и вывести результат.
    println("Задание 6")
